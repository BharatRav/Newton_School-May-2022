static public Node moveZeroes(Node head)
{
       Node dummy1 = new Node(-1);
       Node dummy2 = new Node(-1);

       Node p1 = dummy1; // zero
       Node p2 = dummy2; // non zero
       Node curr=  head;
       while(curr!=null){
           Node temp = curr.next;
           if(curr.data==0){
               p1.next = curr;
               p1 = p1.next;
           }else{
               p2.next = curr;
               p2 = p2.next;
           }
           curr.next = null;
           curr = temp;
       }
       p1.next = dummy2.next;
       return dummy1.next;
}
